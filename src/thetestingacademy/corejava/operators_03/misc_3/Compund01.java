package thetestingacademy.corejava.operators_03.misc_3;

public class Compund01 {
    public static void main(String[] args) {
        int a = 90;
        a += 90; //  a =  a+90;
        System.out.println(a);

        int b = 90;
        b-=89;   //b = b-89;
        System.out.println(b);

    }
}
