package thetestingacademy.corejava.loops_04.whiledemo;

public class WhileDemo02 {
    public static void main(String[] args) {
        // 10 to 1
        // ?  i >= 1
        // code - i
        // i--
        int i = 10;
        while(i>=1){
            System.out.println(i);
            i--;
        }

    }
}
