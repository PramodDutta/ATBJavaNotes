package thetestingacademy.corejava.loops_04.fordemo;

public class For08 {
    public static void main(String[] args) {
        // Nested for  loops
        for (int i = 0; i < 3; i++) {
            for (int j = 0; j < 3; j++) {
                System.out.println(i + " " + j);
            }

        }




    }
}
